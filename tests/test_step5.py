def test_guardian_block_warn_and_pass():
    assert scan_text("safe public text").severity == "pass"
    warn = scan_text("이 자료는 내부자료 검토용")
    assert warn.severity == "warn"
    block = scan_text("삼성 내부 원가 정보")
    assert block.severity == "block"


def test_guardian_scans_raw_results_and_draft():
    state = {"period_id": "2026-06", "raw_results": {"results": [{"raw_text": "confidential"}]}, "draft": "safe"}
    result = scan_state(state)
    assert result.severity == "warn"
    blocked = guardian_node({"period_id": "2026-06", "draft": "삼성 미공개 정보"})
    assert blocked["hard_fail"] is True
    assert blocked["guardian_result"]["severity"] == "block"


def test_hermes_research_adapter_injected_and_validation(tmp_path):
    plan = build_query_plan("2026-26", period_id="2026-06")
    raw = {
        "period_id": "2026-06",
        "week_id": "2026-26",
        "results": [
            {
                "query_id": "q1",
                "source_url": "https://example.com/a",
                "source_date": "2026-06-01",
                "source_type": "official",
                "title": "title",
                "summary": "summary",
                "raw_text": "raw",
                "competitor": "GMCC/Midea",
                "compressor_type": "Re",
                "category": "신제품·라인업",
                "refrigerants": ["R290"],
                "samsung_status": "미보유",
            }
        ],
    }
    path = tmp_path / "raw.json"
    path.write_text(json.dumps(raw, ensure_ascii=False), encoding="utf-8")
    loaded = HermesResearchAdapter(path).search(plan)
    assert loaded["period_id"] == "2026-06"
    assert HermesResearchAdapter.trust_distribution(loaded)["high_confidence"] == 1

    bad_path = tmp_path / "bad.json"
    bad_path.write_text(json.dumps({"results": [{"query_id": "q"}]}), encoding="utf-8")
    with pytest.raises(ValueError):
        HermesResearchAdapter(bad_path).search(plan)


