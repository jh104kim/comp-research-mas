def test_file_vector_store_and_rag_period_search():
    ledger = {
        "periods": {
            "2026-06": {
                "evidence": [
                    {"compressor_type": "Re", "competitor": "GMCC/Midea", "category": "신제품·라인업", "trust_score": 5, "dynamic_tags": ["Re", "신제품·라인업"], "evidence_id": "e1"},
                    {"compressor_type": "Ro", "competitor": "LG", "category": "성능·효율", "trust_score": 3, "dynamic_tags": ["Ro"], "evidence_id": "e2"},
                ]
            }
        }
    }
    found = FileVectorStore(ledger).search("Re", "GMCC/Midea", "신제품·라인업")
    assert found[0]["evidence_id"] == "e1"
    # RAG uses the real ledger generated by step5 sample; just assert callable/list contract.
    assert isinstance(retrieve_related_evidence(compressor_type="Re", competitor="GMCC/Midea", category="신제품·라인업"), list)


def test_debate_points_accepted_rejected_and_not_needed():
    assert build_debate_points(10, [], False) == []
    major = build_debate_points(6, ["출처 목록에 source name/url/date를 포함하세요."], False)
    assert major[0]["severity"] == "major"
    assert decide_debate_points(major)[0]["decision"] == "accepted"
    minor = build_debate_points(8, ["문체를 더 간결하게 조정하세요."], False)
    assert minor[0]["severity"] == "minor"
    assert decide_debate_points(minor)[0]["decision"] == "rejected"


def test_notifier_dry_run_outbox_and_payloads():
    state = {
        "period_id": "2026-06",
        "draft": "# report\n\n## 이번 주 핵심 동향 요약\n- 핵심\n",
        "report_meta": {"high_threat_count": 2, "signal_count": 1},
        "output_paths": {"report": "outputs/reports/2026-06_compressor_monthly.md"},
    }
    result = prepare_notifier_dry_run(state)
    outbox = Path(result["notifier_outbox"])
    assert (outbox / "email_payload.json").exists()
    email = json.loads((outbox / "email_payload.json").read_text(encoding="utf-8"))
    assert email["to"] == "jh104.kim@samsung.com"
    assert email["attachment"].endswith(".md")
    obsidian = json.loads((outbox / "obsidian_payload.json").read_text(encoding="utf-8"))
    assert obsidian["vault_path"]


